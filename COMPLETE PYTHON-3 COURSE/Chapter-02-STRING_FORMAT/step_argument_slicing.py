# slicing  / selecting sub sequences
#syntax - [start argument : stop argument -1 : step]
print("Language"[::]) # Language
print("Language"[1:5:1]) # angu
print("Language"[5:0:-1]) # augna
print("Language"[-1::-1]) # egaugnaL
print("Language"[0::2])   # Lnug
print("Language"[::3])    #  Lgg


lang = "Python"
print(lang[1:5:2]) # yh
print(lang[::1])   # Python
print(lang[-1::-1])   # nohtyP

